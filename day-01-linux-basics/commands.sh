#!/bin/bash

# Show current directory
pwd

# List files
ls

# List all files including hidden with details
ls -la

# Change directory
cd /tmp

# Create a test file
touch testfile.txt

# Copy file
cp testfile.txt copy.txt

# Move/rename file
mv copy.txt moved.txt

# Remove file
rm moved.txt

# Check memory usage
free -h

# Check disk usage
du -h

# Monitor processes
top
